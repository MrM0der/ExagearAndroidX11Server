package com.eltechs.axs.geom;

public class RectangleF {
    public final float height;
    public final float width;
    public final float x;
    public final float y;

    public RectangleF(float f, float f2, float f3, float f4) {
        this.x = f;
        this.y = f2;
        this.width = f3;
        this.height = f4;
    }
}
